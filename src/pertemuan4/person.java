package pertemuan4;

public class person {
    //atribut
    private String nama, alamat;

    //constructor
    public person (String n, String a){
        nama = n;
        alamat = a;
    }

    //getter

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }

}
